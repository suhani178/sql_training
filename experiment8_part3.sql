SELECT 
E.EMPNO AS EMPLOYEE_NUMBER,
E.ENAME AS EMPLOYEE_NAME,
IFNULL (M.EMPNO, '-') AS MANAGER_EMPNO,
IFNULL(M.ENAME, 'NO MANAGER') AS MANAGER_NAME
FROM 
EMPLOYEE E
  LEFT JOIN EMPLOYEE  M ON E.MGR = M.EMPNO;
  
