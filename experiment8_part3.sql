  SELECT
  D.DEPTNO AS DEPARTMENT_NUMBER,
  D.DNAME AS DEPARTMENT_NAME,
  SUM(E.SAL) as TOTAL_SALARY
  FROM
  EMPLOYEE E
     INNER JOIN DEPARTMENT D ON E.DEPTNO = D.DEPTNO
     GROUP BY D.DNAME, D.DEPTNO;
     

